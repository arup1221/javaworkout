package com.ARUP;

public class SquareRoot {
    public static void main(String[] args) {
        int a = 4;
        double b = Math.pow(a, 3);
        double d = Math.pow(a, 0.5);
        double c = Math.sqrt(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
